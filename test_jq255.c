int
main(void)
{
	test_pubkey_decode();
	test_keypair_decode();
	test_sign();
	test_ECDH();

#if defined SPEED_X86
	uint32_t rv;
	printf("Speed benchmark (median, low 10%%, high 10%%):\n");
	printf("WARNING: this is meaningless if you do not have disabled\n");
	printf("frequency scaling (aka \"TurboBoost\").\n");
	fflush(stdout);
	rv = 0;
	rv ^= speed_keygen();
	rv ^= speed_sign();
	rv ^= speed_verify();
	rv ^= speed_ECDH();
	printf("%u\n", (unsigned)(rv & 0xFF));
#endif

	return 0;
}
