static size_t
hextobin(void *dst, size_t dst_len, const char *src)
{
	size_t k = 0;
	int z = 0;
	unsigned acc = 0;
	for (;;) {
		int d = *src ++;
		if (d == 0) {
			if (z) {
				fprintf(stderr, "invalid hex: odd number of digits\n");
				exit(EXIT_FAILURE);
			}
			return k;
		}
		if (d <= 32 || d == '-' || d == ':') {
			continue;
		}
		if (d >= '0' && d <= '9') {
			d -= '0';
		} else if (d >= 'A' && d <= 'F') {
			d -= 'A' - 10;
		} else if (d >= 'a' && d <= 'f') {
			d -= 'a' - 10;
		} else {
			fprintf(stderr, "invalid hex character: %c\n", d);
			exit(EXIT_FAILURE);
		}
		if (z) {
			if (k >= dst_len) {
				fprintf(stderr, "hex decode: overflow\n");
				exit(EXIT_FAILURE);
			}
			((uint8_t *)dst)[k ++] = (acc << 4) | d;
		} else {
			acc = d;
		}
		z = !z;
	}
}

