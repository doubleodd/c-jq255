static void
test_pubkey_decode(void)
{
	printf("Test pubkey decode: ");
	fflush(stdout);

	for (int i = 0; KAT_DECODE_OK[i] != NULL; i ++) {
		uint8_t buf1[32], buf2[32];
		jq_public_key pk;

		HEXTOBIN(buf1, KAT_DECODE_OK[i]);
		if (jq_decode_public_key(&pk, buf1, 32) != 1) {
			fprintf(stderr, "ERR: failed to decode pubkey\n");
			exit(EXIT_FAILURE);
		}
		if (jq_encode_public_key(buf2, &pk) != 32) {
			fprintf(stderr, "ERR: failed to re-encode pubkey\n");
			exit(EXIT_FAILURE);
		}
		if (memcmp(buf1, buf2, 32) != 0) {
			fprintf(stderr, "ERR: decode/encode wrong output\n");
			exit(EXIT_FAILURE);
		}
		printf(".");
		fflush(stdout);
	}
	printf(" ");
	fflush(stdout);
	for (int i = 0; KAT_DECODE_BAD[i] != NULL; i ++) {
		uint8_t buf1[32];
		jq_public_key pk;

		HEXTOBIN(buf1, KAT_DECODE_BAD[i]);
		if (jq_decode_public_key(&pk, buf1, 32) != 0) {
			fprintf(stderr, "ERR: decoded invalid pubkey\n");
			exit(EXIT_FAILURE);
		}
		printf(".");
		fflush(stdout);
	}

	printf(" done.\n");
	fflush(stdout);
}

static void
test_keypair_decode(void)
{
	printf("Test keypair decode: ");
	fflush(stdout);

	for (int i = 0; KAT_KEYPAIR[i] != NULL; i += 2) {
		uint8_t buf[64], tmp[64];
		jq_public_key pk;
		jq_private_key sk;
		jq_keypair jk;

		hextobin(buf, 32, KAT_KEYPAIR[i + 0]);
		hextobin(buf + 32, 32, KAT_KEYPAIR[i + 1]);
		if (jq_decode_private_key(&sk, buf, 32) != 1) {
			fprintf(stderr, "ERR: KEYPAIR: decode privkey\n");
			exit(EXIT_FAILURE);
		}
		jq_make_public(&pk, &sk);
		jq_encode_public_key(tmp, &pk);
		if (memcmp(tmp, buf + 32, 32) != 0) {
			fprintf(stderr, "ERR: KEYPAIR: encode pubkey\n");
			exit(EXIT_FAILURE);
		}
		if (jq_decode_keypair(&jk, buf, 64) != 1) {
			fprintf(stderr, "ERR: KEYPAIR: decode keypair\n");
			exit(EXIT_FAILURE);
		}
		jq_encode_keypair(tmp, &jk);
		if (memcmp(tmp, buf, 64) != 0) {
			fprintf(stderr, "ERR: KEYPAIR: encode keypair\n");
			exit(EXIT_FAILURE);
		}
		buf[63] |= 0x80;
		if (jq_decode_public_key(&pk, buf + 32, 32) != 0) {
			fprintf(stderr, "ERR: KEYPAIR: decode pubkey (2)\n");
			exit(EXIT_FAILURE);
		}
		memset(tmp, 'T', sizeof tmp);
		jq_encode_public_key(tmp, &pk);
		for (int j = 0; j < 32; j ++) {
			if (tmp[j] != 0) {
				fprintf(stderr, "ERR: KEYPAIR: encode pubkey (2)\n");
				exit(EXIT_FAILURE);
			}
		}
		if (jq_decode_keypair(&jk, buf, 64) != 0) {
			fprintf(stderr, "ERR: KEYPAIR: decode keypair (2)\n");
			exit(EXIT_FAILURE);
		}
		memset(tmp, 'T', sizeof tmp);
		jq_encode_keypair(tmp, &jk);
		for (int j = 0; j < 64; j ++) {
			if (tmp[j] != 0) {
				fprintf(stderr, "ERR: KEYPAIR: encode keypair (2)\n");
				exit(EXIT_FAILURE);
			}
		}

		printf(".");
		fflush(stdout);
	}

	printf(" done.\n");
	fflush(stdout);
}

static void
test_sign(void)
{
	printf("Test sign: ");
	fflush(stdout);

	for (int i = 0; KAT_SIGN[i] != NULL; i += 5) {
		uint8_t buf_key[64], buf_seed[20], buf_msg[32], buf_sig[48];
		uint8_t tmp[48];
		size_t seed_len;
		jq_keypair jk;

		hextobin(buf_key, 32, KAT_SIGN[i + 0]);
		hextobin(buf_key + 32, 32, KAT_SIGN[i + 1]);
		seed_len = hextobin(buf_seed, sizeof buf_seed, KAT_SIGN[i + 2]);
		HEXTOBIN(buf_msg, KAT_SIGN[i + 3]);
		HEXTOBIN(buf_sig, KAT_SIGN[i + 4]);
		if (jq_decode_keypair(&jk, buf_key, 64) != 1) {
			fprintf(stderr, "ERR: SIGN: decode keypair\n");
			exit(EXIT_FAILURE);
		}
		if (jq_sign_seeded(tmp, &jk,
			JQ255_HASHNAME_BLAKE2S, buf_msg, 32,
			buf_seed, seed_len) != 48)
		{
			fprintf(stderr, "ERR: SIGN: sig length\n");
			exit(EXIT_FAILURE);
		}
		if (memcmp(tmp, buf_sig, 48) != 0) {
			fprintf(stderr, "ERR: SIGN: sig value\n");
			exit(EXIT_FAILURE);
		}
		if (jq_verify(buf_sig, 48, &jk.public_key,
			JQ255_HASHNAME_BLAKE2S, buf_msg, 32) != 1)
		{
			fprintf(stderr, "ERR: SIGN: verify (1)\n");
			exit(EXIT_FAILURE);
		}
		buf_msg[11] ^= 0x01;
		if (jq_verify(buf_sig, 48, &jk.public_key,
			JQ255_HASHNAME_BLAKE2S, buf_msg, 32) != 0)
		{
			fprintf(stderr, "ERR: SIGN: verify (2)\n");
			exit(EXIT_FAILURE);
		}

		printf(".");
		fflush(stdout);
	}

	printf(" done.\n");
	fflush(stdout);
}

